"""Answers on the questions about venv."""

# 1. Что делает команда python -m venv venv?
#
# Ответ: Создает виртуальное окружение с названием .venv в папке проекта.
#
# 1.1 Что делает каждая команда в списке ниже?
#
#
# ![image.png](attachment:image.png)
#
#
# Ответ: pip list - показывает список библиотек и их версии, которые установлены с помощью этого пакетного менеджера.
#        pip freeze > requirements.txt - эта команда экспортирует список библиотек проекта и их версий в файл requirements.txt.
#        pip install -r requirements.txt - эта команда устанавливает зависимости из списка в файле requirements.txt(библиотеки).
#
# 2. Что делает каждая команда в списке ниже?
#
# ![image-2.png](attachment:image-2.png)
#
# Ответ: conda env list - показывает список библиотек и их версии в этом пакетном менеджере(конда).
#        conda create -n env_name python=3.5 - создает виртуальное окружение с помощью конды с пайтоном версии 3.5.
#        conda env update -n env_name -f file.yml - обновляет текущее окружение и зависимости по файлу file.yml.
#        source activate env_name - активирует уже созданное виртуальное окружение.
#        source deactivate - деактивирует(выходит из него) виртуальное окружение.
#        conda clean -a - удаляет кеш и временные файлы связанные с кондой.
#
