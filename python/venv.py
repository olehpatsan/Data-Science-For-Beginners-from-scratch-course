# 6. Что делают эти команды?
#
# pip install -r requirements.txt - эта команда устанавливает зависимости из списка в файле requirements.txt(библиотеки).
# conda env create -f environment.yml - создает виртуальное окружение конда с помощью файла environment.yml.
#
# 7. Что делают эти команды?
#
# pip list - показывает список библиотек и их версии, которые установлены с помощью этого пакетного менеджера.
# pip show - показывает подробную информацию о конкретной библиотеке.
# conda list - показывает список библиотек и их версии в этом пакетном менеджере(конда).
#
# 8. Где по умолчанию больше пакетов venv/pip или conda? и почему дата сайнинисты используют conda?
#
# Ответ: Conda имеет очень большой пакет библиотек из коробки, что иногда очень удобно, чтобы не тратить время на мануальную установку, а pip позволяет УСТАНОВИТЬ больше библиотек, но изначально не имеет большого количества уже установленных библиотек. ДС'ы используют conda из-за удобства также там легче настроить определенное окружение с разной версией пайтон.
