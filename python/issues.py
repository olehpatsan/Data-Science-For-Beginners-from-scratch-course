"""Issues questions."""

# Общие вопросы
#
# Что такое Issues на GitHub и для чего они используются?
#
# Ответ: Раздел для работы с репозиторием, где можно запросить ревью кода или создать пост с проблемой в коде.
#
# Чем Issues отличаются от других инструментов управления задачами?
#
# Ответ: Это удобный раздел, где можно постить задачи в конкретном месте, которые нужно решить, описать их, указать срок и прям в коммите можно указывать определнный ишьюс который был закрыт.
#
# Какие основные компоненты (поля) есть у каждого Issue?
#
# Ответ: Название, описание, ccылка, добавить видео, добавить скриншоты, ошибка вывода, предполагаемое поведение, текущее поведение, метки, какой проект, кто назначил, приоритет, срок, ветки и пул реквесты линкнутые к этому ишьюсу. Если через выделение кода и создание ищью, то название, описание, метки, какой проект, кто назначил, приоритет, срок, ветки и пул реквесты линкнутые к этому ишьюсу
#
# Создание Issues
#
# Как создать новое Issue в репозитории?
#
# Ответ: Зайти в раздел Issue и нажать New Issue, выбрать тип и заполнить форму. Либо выделить кусок кода в репозитории и "Reference in New Issue".
#
# Какие данные рекомендуется указывать в описании Issue для лучшего понимания задачи?
#
# Ответ: Скопировать ссылку на кусок кода, на который мы хотим ссылаться, и описать проблему.
#
# Какие теги (labels) можно добавить к Issue? Какие из них стандартные?
#
# Ответ: Bug, dependencies, docs, duplicate, enhancement - стандарт, good for first issue, help wanted, invalid - доп.
#
# Как прикрепить Assignees (ответственных) к Issue?
#
# Ответ: Нажать на шестеренку в редактировании или при заполнении ишьюса.
#
# Работа с Issues
#
# Как использовать Labels для классификации задач?
#
# Ответ: Выбрать тег соответствующий проблеме.
#
# Для чего нужен Milestone, и как связать его с Issue?
#
# Ответ: Выбрать в Milestone срок выполнения проблемы.
#
# Как привязать Issue к пул-реквесту (Pull Request)?
#
# Ответ: В описании указать Closes #(номер)
#
# Как добавить комментарий к существующему Issue?
#
# Ответ: Зайти в раздел и открыть нужный ишью и написать коммент.
#
