# Фильтрация и поиск
#
# Как найти все открытые или закрытые Issues в репозитории?
#
# Ответ: Во вкладке Issues, там сверху будут открытые, но можно выбрать и закрытые.
#
# Как использовать фильтры для поиска Issues по меткам, исполнителям или другим критериям?
#
# Ответ: is:open(closed) показывать только открытые или закрытые,
#        author:имя_пользователя,
#        assignee:имя_пользователя,
#        label:имя_метки (label:bug, label:documentation),
#        mentions:имя_пользователя (задачи, где вас или кого-то другого тегнули в комментариях).
#
# Как сортировать Issues по приоритету, дате создания или другим параметрам?
#
# Ответ: Нужно нажать кнопку sort и выбрать нужный тип сортировки.
#
#
# Интеграции и автоматизация
#
# Как настроить автоматические уведомления о новых или изменённых Issues?
#
# Ответ: В репозитории слева от кнопки Fork есть кнопка Watch. С ее помощью мы подписываемся на обновления репозитория, и там можно указать что нас интересует.
#
# Что такое Projects в контексте GitHub, и как связать их с Issues?
#
# Ответ: Это инструмент где можно смотреть на все ишьюсы разом. Связать можно через вкладку Projects добавить item c ишьюсом.
#
# Какие сторонние инструменты можно использовать для автоматизации работы с Issues (например, боты, Webhooks)?
#
# Ответ: Можно добавить инструкции в yml файл в .github/workflows/ через git actions.
#
# Коллаборация
#
# Как упомянуть другого пользователя в комментарии к Issue?
#
# Ответ: через знак @, пример: @имя_пользователя.
#
# Как запросить дополнительные данные или уточнения у автора Issue?
#
# Ответ: Упомянуть автора(@имя_пользователя) в комментарии и попросить уточнить.
#
# Что делать, если Issue неактуально или его нужно объединить с другим?
#
# Ответ: Если задача ценная, нужно ее перенести в другой ишью. Если ишью устарел, нужно тегнуть автора и задать вопрос об актуальности и состоянии ишью перед закрытием.
#
# Практические аспекты
#
# Как использовать шаблоны для создания Issues?
#
# Ответ: Создать папку ISSUE_TEMPLATE в .github и там создать .yml файл с темплейтом.
#
# Что такое Linked Issues, и как создать связь между задачами?
#
# Ответ: Через ключевые слова в описании или комментарии к проблеме. пример: is related to #(номер ишью)
#
# Какие метрики (например, время выполнения) можно отслеживать с помощью Issues?
#
# Ответ: Можно отслеживать время выполнения, время до первого ответа, время ожидания, скорость работы, частоту определенных тегов(их соотношение и их рост по датам).
#
# Какие best practices рекомендуются при работе с Issues в команде?
#
# Ответ: Один ишью - это одна проблема, информативные заголовки и описания, использование шаблонов, использование меток и линков.
#
